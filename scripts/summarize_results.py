from pathlib import Path
import argparse
import csv


SUMMARY_FIELDNAMES = [
    "experiment_name",
    "model",
    "preprocessing",
    "best_d",
    "best_accuracy",
    "best_error",
]


def parse_args():
    parser = argparse.ArgumentParser(
        description="Summarize experiment results by best error per experiment.",
    )
    parser.add_argument(
        "results_csv",
        nargs="?",
        type=Path,
        default=Path("results/final_full_experiment_plan.csv"),
        help="Experiment results CSV generated by scripts/run_experiments.py.",
    )
    parser.add_argument(
        "--output-csv",
        type=Path,
        default=None,
        help="Optional CSV path where the summary is written.",
    )

    return parser.parse_args()


def summarize_results(results_csv: Path) -> list[dict[str, str]]:
    best_rows = {}

    with results_csv.open("r", newline="", encoding="utf-8") as csv_file:
        reader = csv.DictReader(csv_file)

        _validate_columns(reader.fieldnames, results_csv)

        for row in reader:
            experiment_name = row["experiment_name"]
            current_best = best_rows.get(experiment_name)

            if current_best is None or _is_better_result(row, current_best):
                best_rows[experiment_name] = row

    return [
        _summary_row(row)
        for row in sorted(
            best_rows.values(),
            key=lambda item: item["experiment_name"],
        )
    ]


def write_summary(rows: list[dict[str, str]], output_csv: Path) -> None:
    output_csv.parent.mkdir(parents=True, exist_ok=True)

    with output_csv.open("w", newline="", encoding="utf-8") as csv_file:
        writer = csv.DictWriter(csv_file, fieldnames=SUMMARY_FIELDNAMES)
        writer.writeheader()
        writer.writerows(rows)


def print_summary(rows: list[dict[str, str]]) -> None:
    if not rows:
        print("No results found")
        return

    widths = {
        field: max(
            len(field),
            *(len(str(row[field])) for row in rows),
        )
        for field in SUMMARY_FIELDNAMES
    }
    header = _format_table_row(
        {field: field for field in SUMMARY_FIELDNAMES},
        widths,
    )
    separator = " | ".join("-" * widths[field] for field in SUMMARY_FIELDNAMES)

    print(header)
    print(separator)

    for row in rows:
        print(_format_table_row(row, widths))


def main():
    args = parse_args()
    rows = summarize_results(args.results_csv)

    print_summary(rows)

    if args.output_csv is not None:
        write_summary(rows, args.output_csv)
        print()
        print(f"Summary CSV: {args.output_csv}")


def _validate_columns(fieldnames, results_csv: Path) -> None:
    missing_columns = [
        field
        for field in [
            "experiment_name",
            "model",
            "preprocessing",
            "n_components",
            "accuracy",
            "error",
        ]
        if field not in (fieldnames or [])
    ]

    if missing_columns:
        raise ValueError(
            f"{results_csv} is missing required columns: {missing_columns}"
        )


def _is_better_result(
    candidate: dict[str, str],
    current_best: dict[str, str],
) -> bool:
    candidate_error = float(candidate["error"])
    best_error = float(current_best["error"])

    if candidate_error != best_error:
        return candidate_error < best_error

    return int(candidate["n_components"]) < int(current_best["n_components"])


def _summary_row(row: dict[str, str]) -> dict[str, str]:
    return {
        "experiment_name": row["experiment_name"],
        "model": row["model"],
        "preprocessing": row["preprocessing"],
        "best_d": row["n_components"],
        "best_accuracy": _format_float(row["accuracy"]),
        "best_error": _format_float(row["error"]),
    }


def _format_float(value: str) -> str:
    return f"{float(value):.6f}"


def _format_table_row(row: dict[str, str], widths: dict[str, int]) -> str:
    return " | ".join(
        str(row[field]).ljust(widths[field])
        for field in SUMMARY_FIELDNAMES
    )


if __name__ == "__main__":
    main()
